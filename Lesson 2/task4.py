# Для проверки полученного в задании 3 значения ковариации, мы можем использовать функцию
# `np.cov()` из библиотеки `numpy`. Эта функция вычисляет ковариационную матрицу для
# заданного массива.
# ### Пояснения:
# - Создаем массив `a`.
# - Вычисляем ковариационную матрицу с помощью `np.cov(a.T)`. Аргумент `a.T` означает,
# что мы передаем транспонированный массив `a`, так как `np.cov()` ожидает, что каждый столбец
# будет представлять собой отдельную переменную.
# - Извлекаем значение ковариации из полученной ковариационной матрицы. Оно находится в строке с
# индексом 0 и столбце с индексом 1.


import numpy as np

# Создание массива a размером 5x2
a = np.array([[1, 6],
              [2, 8],
              [3, 11],
              [3, 10],
              [1, 7]])

# Вычисление ковариационной матрицы с помощью np.cov()
cov_matrix = np.cov(a.T)

# Извлечение значения ковариации из ковариационной матрицы
cov_value = cov_matrix[0, 1]

print("Исходный массив a:")
print(a)
print("\nКовариационная матрица:")
print(cov_matrix)
print("\nЗначение ковариации:")
print(cov_value)


