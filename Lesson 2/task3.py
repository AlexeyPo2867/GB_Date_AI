# Для решения этого задания нам потребуется вычислить скалярное произведение столбцов массива
# `a_centered`, а затем разделить его на `N-1`, где `N` - число наблюдений.
#
# ### Пояснения:
# - Создаем массив `a`.
# - Вычисляем средние значения по каждому столбцу и сохраняем в `mean_a`.
# - Вычисляем центрированный массив `a_centered`.
# - Вычисляем скалярное произведение столбцов `a_centered` с помощью `np.dot(a_centered.T, a_centered)`.
# - Вычисляем результат, разделив `a_centered_sp` на `N-1`, где `N` - число наблюдений.


import numpy as np

# Создание массива a размером 5x2
a = np.array([[1, 6],
              [2, 8],
              [3, 11],
              [3, 10],
              [1, 7]])

# Нахождение среднего значения по каждому признаку (столбцу)
mean_a = np.mean(a, axis=0)

# Вычисление центрированного массива a_centered
a_centered = a - mean_a

# Вычисление скалярного произведения столбцов a_centered
a_centered_sp = np.dot(a_centered.T, a_centered)

# Вычисление величины, деленной на N-1, где N - число наблюдений
N = a.shape[0]
result = a_centered_sp / (N - 1)

print("Исходный массив a:")
print(a)
print("\nЦентрированный массив a_centered:")
print(a_centered)
print("\nСкалярное произведение столбцов a_centered:", a_centered_sp)
print("\nРезультат, деленный на N-1:", result)

