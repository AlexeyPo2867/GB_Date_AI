# Для того чтобы вычислить массив `a_centered`, необходимо отнять средние значения
# каждого столбца (содержащиеся в массиве `mean_a`) от соответствующих значений в массиве `a`.
# Это можно сделать в одно действие с использованием операций с массивами в `numpy`.

# ### Пояснения:
# - Создаем массив `a`.
# - Вычисляем средние значения по каждому столбцу и сохраняем в `mean_a`.
# - Для центрирования массива (`a_centered`), вычитаем массив `mean_a` из массива `a`. В `numpy` это выполняется автоматически для каждого элемента (broadcasting).
#

import numpy as np

# Создание массива a размером 5x2
a = np.array([[1, 6],
              [2, 8],
              [3, 11],
              [3, 10],
              [1, 7]])

# Нахождение среднего значения по каждому признаку (столбцу)
mean_a = np.mean(a, axis=0)

# Вычисление массива a_centered путем вычитания mean_a из каждого элемента в a
a_centered = a - mean_a

print("Исходный массив a:")
print(a)
print("\nСредние значения по признакам (mean_a):")
print(mean_a)
print("\nЦентрированный массив a_centered:")
print(a_centered)


